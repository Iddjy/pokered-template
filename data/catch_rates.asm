	ld hl, CatchRates
	ld a, [wMonSpeciesTemp]
	ld b, a
	ld a, [wMonSpeciesTemp + 1]
	ld c, a
	dec bc						; species ID start at 1 so need to decrement by 1
	add hl, bc
	ld a, [hl]
	ld [wEnemyMonActualCatchRate], a
	ret
