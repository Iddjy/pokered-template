; input: (dex number - 1) * 2 in de
; output: species ID in [wMonSpeciesTemp/wMonSpeciesTemp+1]
GetSpeciesIDFromDexNumber:
	ld hl, DexNumberToSpeciesIDMappingTable
	add hl, de
	ld a, [hli]
	ld [wMonSpeciesTemp + 1], a
	ld a, [hl]
	ld [wMonSpeciesTemp], a
	ret
